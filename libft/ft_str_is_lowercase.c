#include "libft.h"

int		ft_str_is_lowercase(char *s)
{
	if (!s || !*s)
		return (0);
	while (*s)
	{
		if (*s >= 97 && *s <= 122)
			s++;
		else
			return (0);
	}
	return (1);
}
